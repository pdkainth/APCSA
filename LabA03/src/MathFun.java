public class MathFun {
	
	/** 
	 * Method to the calculations and print the answers
	 */
	
	public void calculate(){
		
		int intAnswer = 4 + 9;
		System.out.println("4 + 9 =  " + intAnswer);
		
		int intAnswer2 = 46 / 7;
		System.out.println("46 / 7 = " + intAnswer2);
		
		int intAnswer3 = 46 % 7;
		System.out.println("46 % 7 = " + intAnswer3);
		
		double doubleAnswer = 2 * 3.0;
		System.out.println("2 * 3.0 = " + doubleAnswer);
		
		double doubleAnswer2 = (double)25 / 4;
		System.out.println("(double)25 / 4 = " + doubleAnswer2);
		
		int intAnswer4 = (int)7.75 + 2;
		System.out.println("(int)7.75 + 2 = " + intAnswer4);
		
		char test = 'P';
		System.out.println("(int)'P' = " + (int)test);
		
		int test2 = 105;
		System.out.println("(char)105 = " + (char)test2);
		
	}

}
